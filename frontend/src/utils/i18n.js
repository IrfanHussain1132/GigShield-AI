/**
 * Localizes a key based on the current application state.
 * Optimization: Uses a simple lookup with fallback to English.
 */
export function t(key) {
  const lang = state.language || 'en';
  return translations[lang]?.[key] || translations['en']?.[key] || key;
}

/**
 * Returns currency formatted according to current language.
 * Optimization: Added helper for consistent formatting.
 */
export function formatCurrency(amount) {
    const lang = state.language || 'en';
    const locales = {
        'en': 'en-IN',
        'te': 'te-IN',
        'hi': 'hi-IN',
        'ta': 'ta-IN'
    };
    return new Intl.NumberFormat(locales[lang] || 'en-IN', {
        style: 'currency',
        currency: 'INR',
        maximumFractionDigits: 0
    }).format(amount);
}

